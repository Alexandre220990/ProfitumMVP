    if (productsError) {
      console.error('‚ùå Erreur r√©cup√©ration produits:', productsError);
      return;
    }

    console.log(`‚úÖ ${products?.length || 0} produits dans le catalogue`);

    // √âtape 7: Cr√©ation des ClientProduitEligible
