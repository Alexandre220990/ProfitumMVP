    const clientData = { email: testUserData.email };
    
    console.log('‚úÖ Validation des param√®tres r√©ussie');

    // √âtape 2: R√©cup√©ration de la session avec supabaseAdmin
    console.log('üîç √âtape 1: R√©cup√©ration de la session...');
    
    const { data: sessionFromDb, error: sessionError } = await supabaseAdmin
      .from('TemporarySession')
      .select('*')
      .eq('session_token', sessionToken)
      .single();

    if (sessionError || !sessionFromDb) {
      console.log('‚ùå Session non trouv√©e:', sessionToken);
      return;
    }

    console.log('‚úÖ Session trouv√©e:', {
      id: sessionFromDb.id,
      session_token: sessionFromDb.session_token,
      completed: sessionFromDb.completed,
      migrated_to_account: sessionFromDb.migrated_to_account
    });

    // √âtape 3: V√©rification que la session n'est pas d√©j√† migr√©e
    if (sessionFromDb.migrated_to_account) {
      console.log('‚ùå Session d√©j√† migr√©e');
      return;
    }

    // √âtape 4: R√©cup√©ration du client avec supabaseAdmin
    console.log('üîç √âtape 2: R√©cup√©ration du client...');
    
    const { data: client, error: clientError } = await supabaseAdmin
