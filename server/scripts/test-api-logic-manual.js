    const { error: deleteClientError } = await supabaseAdmin
      .from('Client')
      .delete()
      .eq('id', client.id);

    if (deleteClientError) {
      console.error('‚ö†Ô∏è Erreur suppression client:', deleteClientError);
    } else {
      console.log('‚úÖ Client supprim√©');
    }

    // Remettre la session comme non migr√©e
